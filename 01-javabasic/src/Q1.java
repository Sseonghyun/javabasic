
public class Q1 {
	// Q.java 클래스파일 생성
	public static void main(String[] args) {
		// main 메서드 생성
		int A; // A에 정수를 넣겠다 (1개 선언)
		A = 5; // A에 5를 넣겠다 (초기값 선정)
		int B, C; // B,C에 정수를 넣겠다 (2개를 한번에 선언)
		B = 10; // B에 10을 넣겠다 (초기값 선정 = 초기화)
	    C = 15; // C에 15를 넣겠다 (초기값 선정 = 초기화)
	    System.out.println(A); // A출력
	    System.out.println(B); // B출력
	    System.out.println(C); // C출력
	    System.out.println(A+B+C); //A+B+C = 출력
	}

}
