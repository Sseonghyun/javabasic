
public class SwitchCase0 {

	public static void main(String[] args) {

        int a = 4;
		
		switch(a) {
		case 5:
			System.out.println("만점입니다.");
			break;
		case 4:
			System.out.println("4점입니다.");	
			break;
		case 3:
			System.out.println("3점입니다.");	
			break;
		case 2:
			System.out.println("2점입니다.");
			break;
		default :
			System.out.println("재시험입니다.");
			break;
			

	}

}
}