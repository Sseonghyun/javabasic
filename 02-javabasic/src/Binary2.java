
public class Binary2 {

	public static void main(String[] args) {
		// 비교 연산자는 크게 대소비교와 단순비교로 나뉨
		// 컴퓨터는 비교 연산자를 명제로 인식하여 참(true) 거짓(false)를 판단해 결과로 보여줌 
		System.out.println(3 > 5);// 3은 5보다 크다
		System.out.println(12 <= 12);// 12는 12와 작거나 같다
		// 대소비교는 >, <, >=, <= 이며 숫자끼리만 비교 가능
		System.out.println("a" != "b");// a와 b는 다르다
		System.out.println("a" == "b");// a와 b는 같다
		System.out.println(5 == 5.0);// 5와 5.0은 같다
		// 단순비교는 ==, != 이며 모든 자료형간 비교 가능
		}

}
